def create_asr_matrix_sheet(attack_df, writer):
    """
    Create 2D matrix sheets showing Attack Success Rate by adversarial type and position
    Separates results into: adv, adv+defense, and adv+defense - adv (difference)
    
    Args:
        attack_df (pd.DataFrame): DataFrame containing attack results
        writer: Excel writer object
    """
    
    # Filter out rows without adversarial type and position (baseline results)
    attack_only_df = attack_df[
        (attack_df['adv_type'].notna()) & 
        (attack_df['adv_type'] != 'unknown') & 
        (attack_df['adv_type'] != 'None') &
        (attack_df['adv_position'].notna()) & 
        (attack_df['adv_position'] != 'unknown') & 
        (attack_df['adv_position'] != 'None')
    ].copy()
    
    if attack_only_df.empty:
        print("No attack data available for ASR matrix")
        return
    
    # Separate data by defense status
    adv_only_df = attack_only_df[attack_only_df['has_defense'] == False].copy()
    adv_defense_df = attack_only_df[attack_only_df['has_defense'] == True].copy()
    
    print(f"Found {len(adv_only_df)} adversarial-only experiments")
    print(f"Found {len(adv_defense_df)} adversarial+defense experiments")
    
    # Create matrices for Overall Attack Success Rate
    matrices_created = []
    
    # 1. Adversarial only (no defense)
    if not adv_only_df.empty:
        try:
            adv_matrix = adv_only_df.pivot_table(
                values='overall_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            ).round(2)
            
            adv_matrix.to_excel(writer, sheet_name='ASR_Adv_Only')
            matrices_created.append('ASR_Adv_Only')
            print(f"Created adversarial-only ASR matrix: {adv_matrix.shape}")
            
        except Exception as e:
            print(f"Error creating adversarial-only ASR matrix: {e}")
    
    # 2. Adversarial + Defense
    if not adv_defense_df.empty:
        try:
            adv_defense_matrix = adv_defense_df.pivot_table(
                values='overall_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            ).round(2)
            
            adv_defense_matrix.to_excel(writer, sheet_name='ASR_Adv_Defense')
            matrices_created.append('ASR_Adv_Defense')
            print(f"Created adversarial+defense ASR matrix: {adv_defense_matrix.shape}")
            
        except Exception as e:
            print(f"Error creating adversarial+defense ASR matrix: {e}")
    
    # 3. Defense Effectiveness (adv+defense - adv)
    if not adv_only_df.empty and not adv_defense_df.empty:
        try:
            # Create matrices with same index/columns for proper subtraction
            adv_matrix_full = adv_only_df.pivot_table(
                values='overall_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            )
            
            adv_defense_matrix_full = adv_defense_df.pivot_table(
                values='overall_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            )
            
            # Align matrices and compute difference
            adv_matrix_aligned, adv_defense_matrix_aligned = adv_matrix_full.align(adv_defense_matrix_full, fill_value=0)
            defense_effectiveness = (adv_defense_matrix_aligned - adv_matrix_aligned).round(2)
            
            defense_effectiveness.to_excel(writer, sheet_name='ASR_Defense_Effect')
            matrices_created.append('ASR_Defense_Effect')
            print(f"Created defense effectiveness matrix: {defense_effectiveness.shape}")
            
        except Exception as e:
            print(f"Error creating defense effectiveness matrix: {e}")
    
    # Create Job-level ASR matrices
    # 1. Job-level Adversarial only
    if not adv_only_df.empty:
        try:
            job_adv_matrix = adv_only_df.pivot_table(
                values='job_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            ).round(2)
            
            job_adv_matrix.to_excel(writer, sheet_name='Job_ASR_Adv_Only')
            matrices_created.append('Job_ASR_Adv_Only')
            
        except Exception as e:
            print(f"Error creating job-level adversarial-only ASR matrix: {e}")
    
    # 2. Job-level Adversarial + Defense
    if not adv_defense_df.empty:
        try:
            job_adv_defense_matrix = adv_defense_df.pivot_table(
                values='job_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            ).round(2)
            
            job_adv_defense_matrix.to_excel(writer, sheet_name='Job_ASR_Adv_Defense')
            matrices_created.append('Job_ASR_Adv_Defense')
            
        except Exception as e:
            print(f"Error creating job-level adversarial+defense ASR matrix: {e}")
    
    # 3. Job-level Defense Effectiveness
    if not adv_only_df.empty and not adv_defense_df.empty:
        try:
            job_adv_matrix_full = adv_only_df.pivot_table(
                values='job_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            )
            
            job_adv_defense_matrix_full = adv_defense_df.pivot_table(
                values='job_attack_success_rate',
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            )
            
            # Align matrices and compute difference
            job_adv_aligned, job_adv_defense_aligned = job_adv_matrix_full.align(job_adv_defense_matrix_full, fill_value=0)
            job_defense_effectiveness = (job_adv_defense_aligned - job_adv_aligned).round(2)
            
            job_defense_effectiveness.to_excel(writer, sheet_name='Job_ASR_Defense_Effect')
            matrices_created.append('Job_ASR_Defense_Effect')
            
        except Exception as e:
            print(f"Error creating job-level defense effectiveness matrix: {e}")
    
    # Create detailed breakdown tables
    try:
        breakdown_cols = [
            'adv_type', 'adv_position', 'has_defense', 'overall_attack_success_rate', 'job_attack_success_rate',
            'total_jobs', 'total_candidates', 'jobs_attacked', 'total_successful_attacks',
            'POTENTIAL_to_STRONG', 'NOT_to_POTENTIAL', 'NOT_to_STRONG'
        ]
        
        available_breakdown_cols = [col for col in breakdown_cols if col in attack_only_df.columns]
        breakdown_df = attack_only_df[available_breakdown_cols].copy()
        
        # Sort by type, position, and defense status
        breakdown_df = breakdown_df.sort_values(['adv_type', 'adv_position', 'has_defense'])
        
        # Save detailed breakdown
        breakdown_df.to_excel(writer, sheet_name='ASR_Detailed_Breakdown', index=False)
        matrices_created.append('ASR_Detailed_Breakdown')
        
    except Exception as e:
        print(f"Error creating detailed breakdown: {e}")
    
    # Create LaTeX tables
    create_latex_combined_table(adv_only_df, adv_defense_df, writer)
    
    print(f"Successfully created {len(matrices_created)} ASR matrix sheets: {matrices_created}")

def create_latex_combined_table(adv_only_df, adv_defense_df, writer):
    """
    Create LaTeX table combining adv_only, adv_defense, and defense_effect
    in format: adv_only/adv_defense/defense_effect
    Supports both single-model and multi-model outputs

    Args:
        adv_only_df (pd.DataFrame): DataFrame with adversarial-only results
        adv_defense_df (pd.DataFrame): DataFrame with adversarial+defense results
        writer: Excel writer object
    """
    
    if adv_only_df.empty or adv_defense_df.empty:
        print("Cannot create LaTeX table: missing adversarial-only or adversarial+defense data")
        return
    
    try:
        # Check if we have multiple models
        unique_models_adv = adv_only_df['model'].nunique() if 'model' in adv_only_df.columns else 1
        unique_models_def = adv_defense_df['model'].nunique() if 'model' in adv_defense_df.columns else 1
        has_multiple_models = unique_models_adv > 1 or unique_models_def > 1
        
        if has_multiple_models:
            print(f"Creating multi-model LaTeX tables ({unique_models_adv} adv models, {unique_models_def} defense models)")
            create_multi_model_latex_tables(adv_only_df, adv_defense_df, writer)
        else:
            print("Creating single-model LaTeX tables")
            create_single_model_latex_tables(adv_only_df, adv_defense_df, writer)
            
    except Exception as e:
        print(f"Error creating LaTeX tables: {e}")

def create_single_model_latex_tables(adv_only_df, adv_defense_df, writer):
    """
    Create LaTeX tables for single model (original functionality)
    """
    try:
        # Create matrices for both overall and job-level ASR
        for metric_name, metric_col in [('Overall', 'overall_attack_success_rate'), ('Job_Level', 'job_attack_success_rate')]:
            
            # Create individual matrices
            adv_matrix = adv_only_df.pivot_table(
                values=metric_col,
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            )
            
            adv_defense_matrix = adv_defense_df.pivot_table(
                values=metric_col,
                index='adv_type',
                columns='adv_position',
                aggfunc='mean',
                fill_value=0
            )
            
            # Align matrices and compute defense effect
            adv_aligned, adv_defense_aligned = adv_matrix.align(adv_defense_matrix, fill_value=0)
            defense_effect = adv_defense_aligned - adv_aligned 
            
            # Create combined matrix in the format: adv_only/adv_defense/defense_effect
            combined_matrix = adv_aligned.copy().astype(str)
            
            for idx in adv_aligned.index:
                for col in adv_aligned.columns:
                    adv_val = adv_aligned.loc[idx, col]
                    def_val = adv_defense_aligned.loc[idx, col]
                    eff_val = defense_effect.loc[idx, col]
                    
                    # Format as requested: adv_only/adv_defense/defense_effect
                    combined_matrix.loc[idx, col] = f"{adv_val:.2f}/{def_val:.2f}/{eff_val:+.2f}"
            
            # Generate LaTeX table
            latex_table = generate_latex_table(combined_matrix, metric_name)
            
            # Save LaTeX table to a text file via Excel (using a text sheet)
            latex_df = pd.DataFrame([latex_table], columns=['LaTeX_Table'])
            latex_df.to_excel(writer, sheet_name=f'LaTeX_{metric_name}_ASR', index=False)
            
            # Also save to a .tex file
            output_file = f'latex_table_{metric_name.lower()}_asr.tex'
            with open(output_file, 'w') as f:
                f.write(latex_table)
            
            print(f"Created LaTeX table for {metric_name} ASR: {output_file}")
            
    except Exception as e:
        print(f"Error creating single-model LaTeX tables: {e}")

def create_multi_model_latex_tables(adv_only_df, adv_defense_df, writer):
    """
    Create LaTeX tables for multiple models with model-specific and aggregated views
    """
    try:
        # Get unique models from both dataframes
        models_adv = set(adv_only_df['model'].unique() if 'model' in adv_only_df.columns else [])
        models_def = set(adv_defense_df['model'].unique() if 'model' in adv_defense_df.columns else [])
        all_models = sorted(list(models_adv.union(models_def)))
        
        print(f"Found models: {all_models}")
        
        # Create matrices for both overall and job-level ASR
        for metric_name, metric_col in [('Overall', 'overall_attack_success_rate'), ('Job_Level', 'job_attack_success_rate')]:
            
            # 1. Create individual model tables
            model_matrices = {}
            for model in all_models:
                print(f"Processing model: {model} for {metric_name}")
                
                model_adv_df = adv_only_df[adv_only_df['model'] == model] if 'model' in adv_only_df.columns else adv_only_df
                model_def_df = adv_defense_df[adv_defense_df['model'] == model] if 'model' in adv_defense_df.columns else adv_defense_df
                
                if model_adv_df.empty or model_def_df.empty:
                    print(f"Skipping model {model} - missing data")
                    continue
                
                # Create matrices for this model
                adv_matrix = model_adv_df.pivot_table(
                    values=metric_col,
                    index='adv_type',
                    columns='adv_position',
                    aggfunc='mean',
                    fill_value=0
                )
                
                adv_defense_matrix = model_def_df.pivot_table(
                    values=metric_col,
                    index='adv_type',
                    columns='adv_position',
                    aggfunc='mean',
                    fill_value=0
                )
                
                if adv_matrix.empty or adv_defense_matrix.empty:
                    print(f"Skipping model {model} - empty matrices")
                    continue
                
                # Align matrices and compute defense effect
                adv_aligned, adv_defense_aligned = adv_matrix.align(adv_defense_matrix, fill_value=0)
                defense_effect = adv_defense_aligned - adv_aligned
                
                model_matrices[model] = {
                    'adv': adv_aligned,
                    'defense': adv_defense_aligned,
                    'effect': defense_effect
                }
                
                # Create combined matrix for this model
                combined_matrix = adv_aligned.copy().astype(str)
                for idx in adv_aligned.index:
                    for col in adv_aligned.columns:
                        adv_val = adv_aligned.loc[idx, col]
                        def_val = adv_defense_aligned.loc[idx, col]
                        eff_val = defense_effect.loc[idx, col]
                        combined_matrix.loc[idx, col] = f"{adv_val:.2f}/{def_val:.2f}/{eff_val:+.2f}"
                
                # Generate LaTeX table for this model
                model_safe_name = model.replace('/', '_').replace('-', '_')
                latex_table = generate_latex_table(combined_matrix, f"{metric_name} - {model}")
                
                # Save to Excel
                latex_df = pd.DataFrame([latex_table], columns=['LaTeX_Table'])
                latex_df.to_excel(writer, sheet_name=f'LaTeX_{metric_name}_{model_safe_name}', index=False)
                
                # Save to .tex file
                output_file = f'latex_table_{metric_name.lower()}_{model_safe_name}_asr.tex'
                with open(output_file, 'w') as f:
                    f.write(latex_table)
                
                print(f"Created LaTeX table for {model}: {output_file}")
            
            # 2. Create aggregated multi-model table
            if len(model_matrices) > 1:
                print(f"Creating aggregated multi-model table for {metric_name}")
                multi_model_latex = generate_multi_model_latex_table(model_matrices, metric_name, all_models)
                
                # Save aggregated table
                latex_df = pd.DataFrame([multi_model_latex], columns=['LaTeX_Table'])
                latex_df.to_excel(writer, sheet_name=f'LaTeX_{metric_name}_MultiModel', index=False)
                
                output_file = f'latex_table_{metric_name.lower()}_multi_model_asr.tex'
                with open(output_file, 'w') as f:
                    f.write(multi_model_latex)
                
                print(f"Created multi-model LaTeX table: {output_file}")
            
    except Exception as e:
        print(f"Error creating multi-model LaTeX tables: {e}")

def generate_multi_model_latex_table(model_matrices, metric_name, all_models):
    """
    Generate LaTeX table that compares results across multiple models
    """
    try:
        # Get all attack types and positions from the first model
        first_model = next(iter(model_matrices.keys()))
        attack_types = list(model_matrices[first_model]['adv'].index)
        positions = list(model_matrices[first_model]['adv'].columns)
        
        # Create table structure
        n_models = len(all_models)
        n_positions = len(positions)
        
        # Column specification: attack type + model columns for each position
        col_spec = 'l' + 'c' * (n_models * n_positions)
        
        latex_lines = [
            "\\begin{table}[htbp]",
            "\\centering",
            f"\\caption{{Multi-Model {metric_name} Attack Success Rate Comparison}}",
            "\\resizebox{\\textwidth}{!}{",
            f"\\begin{{tabular}}{{{col_spec}}}",
            "\\toprule"
        ]
        
        # Create header with model names under each position
        header_line1 = "Attack Type"
        header_line2 = ""
        
        for pos in positions:
            formatted_pos = pos.replace('_', ' ').title()
            # Add multicolumn header for position
            header_line1 += f" & \\multicolumn{{{n_models}}}{{c}}{{{formatted_pos}}}"
            # Add model names under each position
            if not header_line2:
                header_line2 = " "
            for model in all_models:
                model_short = model.split('/')[-1] if '/' in model else model
                header_line2 += f" & {model_short}"
        
        header_line1 += " \\\\"
        header_line2 += " \\\\"
        
        latex_lines.extend([header_line1, header_line2, "\\midrule"])
        
        # Add data rows
        for attack_type in attack_types:
            formatted_attack = attack_type.replace('_', ' ').title()
            row_line = formatted_attack
            
            for pos in positions:
                for model in all_models:
                    if model in model_matrices and attack_type in model_matrices[model]['adv'].index and pos in model_matrices[model]['adv'].columns:
                        adv_val = model_matrices[model]['adv'].loc[attack_type, pos]
                        def_val = model_matrices[model]['defense'].loc[attack_type, pos]
                        eff_val = model_matrices[model]['effect'].loc[attack_type, pos]
                        cell_value = f"{adv_val:.1f}/{def_val:.1f}/{eff_val:+.1f}"
                    else:
                        cell_value = "-/-/-"
                    row_line += f" & {cell_value}"
            
            row_line += " \\\\"
            latex_lines.append(row_line)
        
        # End table
        latex_lines.extend([
            "\\bottomrule",
            "\\end{tabular}",
            "}",
            "\\end{table}"
        ])
        
        return '\n'.join(latex_lines)
        
    except Exception as e:
        print(f"Error generating multi-model LaTeX table: {e}")
        return f"% Error generating table: {e}"

def generate_latex_table(combined_matrix, metric_name):
    """
    Generate LaTeX table code from combined matrix
    
    Args:
        combined_matrix (pd.DataFrame): Combined matrix with format adv_only/adv_defense/defense_effect
        metric_name (str): Name of the metric (Overall or Job_Level) or with model name
        
    Returns:
        str: LaTeX table code
    """
    
    # Get dimensions
    n_cols = len(combined_matrix.columns)
    col_names = list(combined_matrix.columns)
    row_names = list(combined_matrix.index)
    
    # Create column specification (c for center alignment)
    col_spec = 'l' + 'c' * n_cols  # l for row labels, c for data columns
    
    # Format the metric name for LaTeX (escape special characters and format model names)
    formatted_metric_name = format_model_name_for_latex(metric_name)
    
    # Start LaTeX table
    latex_lines = [
        "\\begin{table}[htbp]",
        "\\centering",
        f"\\caption{{{formatted_metric_name} Attack Success Rate: Adv Only / Adv+Defense / Defense Effect}}",
        f"\\begin{{tabular}}{{{col_spec}}}",
        "\\toprule"
    ]
    
    # Add header row
    header = "Attack Type"
    for col in col_names:
        # Replace underscores with spaces and capitalize
        formatted_col = col.replace('_', ' ').title()
        header += f" & {formatted_col}"
    header += " \\\\"
    latex_lines.append(header)
    latex_lines.append("\\midrule")
    
    # Add data rows
    for row_name in row_names:
        # Format row name (replace underscores with spaces and capitalize)
        formatted_row = row_name.replace('_', ' ').title()
        row_line = formatted_row
        
        for col_name in col_names:
            cell_value = combined_matrix.loc[row_name, col_name]
            row_line += f" & {cell_value}"
        
        row_line += " \\\\"
        latex_lines.append(row_line)
    
    # End table
    latex_lines.extend([
        "\\bottomrule",
        "\\end{tabular}",
        "\\end{table}"
    ])
    
    return '\n'.join(latex_lines)

def format_model_name_for_latex(metric_name):
    """
    Format model names in metric_name for LaTeX display
    
    Args:
        metric_name (str): Metric name that may contain model names
        
    Returns:
        str: LaTeX-formatted metric name
    """
    # Handle common model name patterns
    formatted_name = metric_name
    
    # Replace common model path patterns
    model_replacements = {
        'Qwen/Qwen3-8B': 'Qwen3-8B',
        'deepseek-ai/DeepSeek-R1-Distill-Llama-8B': 'DeepSeek-R1-Distill-Llama-8B',
        'meta-llama/Llama-3.1-8B-Instruct': 'Llama-3.1-8B-Instruct',
        'gemini-2.5-flash': 'Gemini-2.5-Flash'
    }
    
    for full_name, short_name in model_replacements.items():
        formatted_name = formatted_name.replace(full_name, short_name)
    
    # Escape LaTeX special characters
    latex_escapes = {
        '_': '\\_',
        '%': '\\%',
        '$': '\\$',
        '#': '\\#',
        '&': '\\&'
    }
    
    for char, escape in latex_escapes.items():
        formatted_name = formatted_name.replace(char, escape)
    
    return formatted_name

def create_cross_model_latex_tables(all_results):
    """
    Create LaTeX tables that compare results across multiple models
    
    Args:
        all_results (list): List of all result dictionaries from analysis
    """
    try:
        # Filter attack results only
        attack_results = [r for r in all_results if r.get('type') == 'Attack Analysis']
        
        if not attack_results:
            print("No attack results found for cross-model analysis")
            return
        
        # Check if we have multiple models
        unique_models = set(r.get('model', 'unknown') for r in attack_results if r.get('model'))
        if len(unique_models) <= 1:
            print(f"Only {len(unique_models)} unique model(s) found: {unique_models}. Skipping cross-model LaTeX tables.")
            return
        
        print(f"Creating cross-model LaTeX tables for {len(unique_models)} models: {sorted(unique_models)}")
        
        # Convert to DataFrame for easier manipulation
        import pandas as pd
        df = pd.DataFrame(attack_results)
        
        # Filter out results without proper adversarial info
        df_filtered = df[
            (df['adv_type'].notna()) & 
            (df['adv_type'] != 'unknown') & 
            (df['adv_type'] != 'None') &
            (df['adv_position'].notna()) & 
            (df['adv_position'] != 'unknown') & 
            (df['adv_position'] != 'None')
        ].copy()
        
        if df_filtered.empty:
            print("No valid adversarial data found for cross-model analysis")
            return
        
        # Separate adversarial-only and adversarial+defense results
        adv_only = df_filtered[df_filtered['has_defense'] == False]
        adv_defense = df_filtered[df_filtered['has_defense'] == True]
        
        if adv_only.empty or adv_defense.empty:
            print("Missing adversarial-only or adversarial+defense data for cross-model analysis")
            return
        
        # Create cross-model LaTeX tables for both metrics
        for metric_name, metric_col in [('Overall', 'overall_attack_success_rate'), ('Job_Level', 'job_attack_success_rate')]:
            print(f"Creating cross-model table for {metric_name} ASR")
            
            # Create matrices for each model
            model_matrices = {}
            for model in sorted(unique_models):
                model_adv = adv_only[adv_only['model'] == model]
                model_def = adv_defense[adv_defense['model'] == model]
                
                if model_adv.empty or model_def.empty:
                    print(f"Skipping model {model} - insufficient data")
                    continue
                
                # Create pivot tables
                try:
                    adv_matrix = model_adv.pivot_table(
                        values=metric_col,
                        index='adv_type',
                        columns='adv_position',
                        aggfunc='mean',
                        fill_value=0
                    )
                    
                    def_matrix = model_def.pivot_table(
                        values=metric_col,
                        index='adv_type',
                        columns='adv_position',
                        aggfunc='mean',
                        fill_value=0
                    )
                    
                    # Align matrices
                    adv_aligned, def_aligned = adv_matrix.align(def_matrix, fill_value=0)
                    effect_matrix = def_aligned - adv_aligned
                    
                    model_matrices[model] = {
                        'adv': adv_aligned,
                        'defense': def_aligned,
                        'effect': effect_matrix
                    }
                    
                except Exception as e:
                    print(f"Error creating matrices for model {model}: {e}")
                    continue
            
            if len(model_matrices) > 1:
                # Generate cross-model LaTeX table
                latex_content = generate_cross_model_latex_table(model_matrices, metric_name, sorted(unique_models))
                
                # Save to file
                filename = f'latex_table_{metric_name.lower()}_cross_model_asr.tex'
                with open(filename, 'w') as f:
                    f.write(latex_content)
                
                print(f"Created cross-model LaTeX table: {filename}")
            else:
                print(f"Insufficient models with complete data for {metric_name} cross-model table")
                
    except Exception as e:
        print(f"Error creating cross-model LaTeX tables: {e}")

def generate_cross_model_latex_table(model_matrices, metric_name, all_models):
    """
    Generate readable LaTeX tables comparing results across multiple models
    Creates separate tables for each position to improve readability
    
    Args:
        model_matrices (dict): Dictionary with model matrices
        metric_name (str): Name of the metric (Overall or Job_Level)
        all_models (list): List of all model names
        
    Returns:
        str: LaTeX table code with multiple sub-tables
    """
    try:
        # Get attack types and positions from first available model
        first_model = next(iter(model_matrices.keys()))
        attack_types = sorted(list(model_matrices[first_model]['adv'].index))
        positions = sorted(list(model_matrices[first_model]['adv'].columns))
        
        all_latex_content = []
        
        # Create a table for each position to make them more readable
        for pos_idx, pos in enumerate(positions):
            formatted_pos = pos.replace('_', ' ').title()
            
            # Start this position's table
            position_lines = [
                "\\begin{table}[htbp]",
                "\\centering",
                f"\\caption{{Cross-Model {metric_name} Attack Success Rate at {formatted_pos} Position: Adv/Defense/Effect}}",
                f"\\label{{tab:cross_model_{metric_name.lower()}_{pos}_asr}}",
                f"\\begin{{tabular}}{{l{'c' * len(attack_types)}}}",
                "\\toprule"
            ]
            
            # Create header with attack type names
            header_line = "Model"
            for attack_type in attack_types:
                formatted_attack = attack_type.replace('_', ' ').title()
                header_line += f" & {formatted_attack}"
            header_line += " \\\\"
            
            position_lines.extend([header_line, "\\midrule"])
            
            # Add data rows for this position (models as rows)
            for model in all_models:
                if model in model_matrices:
                    model_short = format_model_name_for_latex(model.split('/')[-1] if '/' in model else model)
                    row_line = model_short
                else:
                    row_line = "-"
                
                for attack_type in attack_types:
                    if (model in model_matrices and 
                        attack_type in model_matrices[model]['adv'].index and 
                        pos in model_matrices[model]['adv'].columns):
                        
                        adv_val = model_matrices[model]['adv'].loc[attack_type, pos]
                        def_val = model_matrices[model]['defense'].loc[attack_type, pos]
                        eff_val = model_matrices[model]['effect'].loc[attack_type, pos]
                        
                        cell_value = f"{adv_val:.1f}/{def_val:.1f}/{eff_val:+.1f}"
                    else:
                        cell_value = "-/-/-"
                    
                    row_line += f" & {cell_value}"
                
                row_line += " \\\\"
                position_lines.append(row_line)
            
            # End this position's table
            position_lines.extend([
                "\\bottomrule",
                "\\end{tabular}",
                "\\end{table}"
            ])
            
            all_latex_content.extend(position_lines)
            if pos_idx < len(positions) - 1:
                all_latex_content.append("")  # Empty line between tables
        
        return '\n'.join(all_latex_content)
        
    except Exception as e:
        print(f"Error generating cross-model LaTeX table: {e}")
        return f"% Error generating cross-model table: {e}"

