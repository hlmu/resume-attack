"""
Analyze job classification results from adversarial attacks and defenses.

This script automatically discovers and analyzes result files generated by run.sh and run_template.sh.
It supports:

1. Auto-discovery of result files in the results/ directory
2. Automatic pairing of baseline vs adversarial configurations  
3. Analysis of defense effectiveness
4. Excel output with detailed metrics

Usage:
    # Auto-analyze all files in results directory
    python eval/analyze_results.py
    
    # Specify custom results directory
    python eval/analyze_results.py --results-dir my_results/
    
    # Manually specify file pairs
    python eval/analyze_results.py --manual-pairs file1.json file2.json file3.json None
    
    # Custom output file
    python eval/analyze_results.py --output my_analysis.xlsx

Expected filename patterns from run_template.sh:
- Baseline: results_d_{dataset}_m_{model}_{baseline_normal|baseline_defense_only}.json
- Adversarial: results_d_{dataset}_m_{model}_{lora_}adv_{type}_{position}.json  
- Adversarial+Defense: results_d_{dataset}_m_{model}_{lora_}adv_{type}_{position}_defense.json
"""

